package Ejercicio_1;

public class Base {
    private String nombre;
    private int numAmbulancias;
    private int tiempoMedio;

    public Base(){
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getNumAmbulancias() {
        return numAmbulancias;
    }

    public void setNumAmbulancias(int numAmbulancias) {
        this.numAmbulancias = numAmbulancias;
    }

    public int getTiempoMedio() {
        return tiempoMedio;
    }

    public void setTiempoMedio(int tiempoMedio) {
        this.tiempoMedio = tiempoMedio;
    }
}
